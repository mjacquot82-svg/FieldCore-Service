#!/usr/bin/env node

import { writeFile } from 'node:fs/promises';

const OUTPUT_FILE = new URL('../fieldcore-runtime-config.js', import.meta.url);
const VALID_MODES = new Set(['demo', 'production']);

function normalizeMode(value) {
  const mode = String(value || '').trim().toLowerCase();
  if (mode === 'prod') return 'production';
  if (VALID_MODES.has(mode)) return mode;
  return '';
}

function normalizeUrl(value) {
  return String(value || '').trim().replace(/\/+$/, '');
}

function readEnv() {
  const mode = normalizeMode(process.env.FIELDCORE_APP_MODE);
  const url = normalizeUrl(process.env.FIELDCORE_SUPABASE_URL || process.env.SUPABASE_URL);
  const anonKey = String(process.env.FIELDCORE_SUPABASE_ANON_KEY || process.env.SUPABASE_ANON_KEY || '').trim();

  return {
    mode,
    rawMode: String(process.env.FIELDCORE_APP_MODE || '').trim(),
    url,
    anonKey
  };
}

function decodeJwtPayload(token) {
  const [, payload] = String(token || '').split('.');
  if (!payload) return null;

  try {
    const normalized = payload.replace(/-/g, '+').replace(/_/g, '/');
    const padded = normalized.padEnd(Math.ceil(normalized.length / 4) * 4, '=');
    return JSON.parse(Buffer.from(padded, 'base64').toString('utf8'));
  } catch {
    return null;
  }
}

function assertSafePublicConfig({ mode, rawMode, url, anonKey }) {
  if (rawMode && !mode) {
    throw new Error('FIELDCORE_APP_MODE must be "demo" or "production".');
  }

  if (mode === 'production') {
    if (!url) throw new Error('FIELDCORE_SUPABASE_URL is required when FIELDCORE_APP_MODE=production.');
    if (!anonKey) throw new Error('FIELDCORE_SUPABASE_ANON_KEY is required when FIELDCORE_APP_MODE=production.');
  }

  if (anonKey) {
    const payload = decodeJwtPayload(anonKey);
    if (payload?.role === 'service_role') {
      throw new Error('FIELDCORE_SUPABASE_ANON_KEY must not be a Supabase service_role key.');
    }
  }
}

function buildConfigSource({ mode, url, anonKey }) {
  const generatedAt = mode || url || anonKey ? new Date().toISOString() : null;
  const lines = [
    'window.FIELDCORE_RUNTIME_CONFIG = Object.freeze({ generatedAt: ' + JSON.stringify(generatedAt) + ' });'
  ];

  if (mode) {
    lines.push(`window.FIELDCORE_APP_MODE = ${JSON.stringify(mode)};`);
  }

  if (url && anonKey) {
    lines.push(`window.FIELDCORE_SUPABASE_CONFIG = Object.freeze(${JSON.stringify({
      mode: mode || 'production',
      url,
      anonKey
    }, null, 2)});`);
  }

  return `/* Generated by scripts/generate-runtime-config.mjs. Do not put private keys in this file. */\n(function () {\n  ${lines.join('\n  ')}\n}());\n`;
}

const config = readEnv();
assertSafePublicConfig(config);
await writeFile(OUTPUT_FILE, buildConfigSource(config), 'utf8');

console.log(`Generated ${OUTPUT_FILE.pathname}`);
